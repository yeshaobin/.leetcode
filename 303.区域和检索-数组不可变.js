/**
 * @param {number[]} nums
 */
var NumArray = function(nums) {
    const sums = [0];
    for(let i=1;i<=nums.length;i++){
        sums[i] = sums[i-1] + nums[i-1];
    }
    this.sums = sums;

};

/** 
 * @param {number} left 
 * @param {number} right
 * @return {number}
 */
NumArray.prototype.sumRange = function(left, right) {
    return this.sums[right+1] - this.sums[left]
};

